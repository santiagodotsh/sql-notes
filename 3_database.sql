--------------------------------------------
-- CREATE DATABASE (creates a database)
--------------------------------------------

/*
  CREATE DATABASE is used to create a database.
  The database name is specified after the CREATE DATABASE keywords.
*/

CREATE DATABASE mydatabase; -- Creates a database named mydatabase

--------------------------------------------
-- DROP DATABASE (deletes a database)
--------------------------------------------

/*
  DROP DATABASE is used to delete a database.
  The database name is specified after the DROP DATABASE keywords.
*/

DROP DATABASE mydatabase; -- Deletes a database named mydatabase
